<h3>Types of Computers & Applications
</h3>
<p>
<b>Desktop </b> –
A desktop is a personal computer that is designed to remain mostly fixed at one permanent location. It usually includes a monitor, a keyboard, a mouse and a tower. Due to the presence of several bulky components, it is difficult to move around. However, desktops are usually less expensive than portable computers.
</p><p>

<b>Laptop </b> –
A laptop is a personal computer that is designed to be portable and moved around easily. It typically contains the same components as a desktop, but it is compact in a way that makes it portable. Laptops are great for computing on the go. However, they must remain charged, or else they will shut down when the battery runs out. Also, laptops tend to cost more than desktops.
</p><p>
<b>Smartphones</b> –
A smartphone is a small mobile device which combines the features of a personal computer with the features of a cell phone. They can usually it into one’s pocket, and it can access the Internet, make phone calls, run applications and more. Smartphones offer extreme convenience for moments when it would be impractical to carry around a laptop. They can be operated quickly and efficiently in many different locations. However, smartphones are not usually proficient when it comes to using a word processor or printing. Smartphone users must also be wary of a limited battery life.
</p><p>
<p><b>Internet </b> -
The Internet is the globalized system of computer networks. People use the internet every day to reach a massive network that connects billions of computer devices across the globe. By utilizing the internet, people can quickly share information with others in completely different parts of the world in mere seconds.<p>
<p>
  <b>Word Processor</b>
  – A word processor is a computer application that allows for the creation, editing, formatting and printing of documents. People use word processors to create letters, lists and other documents that previously had to be written by hand or typed using a typewriter. One of the most commonly used word processors today is Microsoft Word.
 </p>
