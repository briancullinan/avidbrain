<h3>Test Preparation  Tutors</h3>
<p>
A <b>Test Preparation  </b>
A <b>test</b> is an assessment designed to measure one’s knowledge and skill. In academics, tests often play a major role in determining one’s final grade. Therefore, the difference between doing well and doing poorly on tests can often represent the difference between passing and failing a course. Additionally, many colleges and other institutions use the results of
<b> standardized tests</b> in order to determine which students to accept into their programs.
</p>
<p>
With the large amount of importance placed upon tests, it is common for students to prepare rigorously for an upcoming examination. This includes both studying material that is likely to appear on the test and practicing various test-taking strategies. Many students even seek outside assistance from tutors in preparing for tests. This preparation usually pays off, as it has been shown that most students who study and prepare for tests beforehand typically score higher than students who do not prepare for tests.
</p>
<h3>List of Standardized Tests  </h3>
<p>
<b>SAT </b> –
The SAT is a standardized test generally used for college admissions. It includes sections covering mathematics, critical reading and writing. Nearly 1.7 million students took the SAT in 2015.

</p>
<p>
<b>ACT </b> –
The ACT is a standardized used to determine high school achievement and college readiness. Colleges often use ACT scores during their admissions process. The ACT consists of sections in English, mathematics, reading, science and an optional writing section. More than 1.8 million students took the ACT in 2014.

</p><p>
<b>GMAT  </b> –
The General Management Admission Test, or GMAT, is a standardized test used in admissions for graduate-level management programs, such as an MBA. It is designed to assess analytical, writing, quantitative, verbal, and reading skills. Roughly 250,000 students per year take the GMAT.

</p>

<p>
<b>GRE  </b> -
The Graduate Record Examinations, or GRE, is a standardized test taken by students applying for graduate school. The exam measures verbal reasoning, quantitative reasoning, analytical writing, and critical thinking skills. More than 650,000 students took the GRE in 2012.

</p>

<p>
<b>MCAT   </b> -
The Medical College Admission Test, or MCAT, is a standardized test taken by students applying for medical school. The exam measures problem solving, critical thinking, written analysis and knowledge of scientific principles and concepts. Since 2007, the exam has been entirely computer-based.
</p>
<p>
<b>LSAT </b> -
The Law School Admission Test, or LSAT, is a standardized test taken by students applying for law school. The exam measures reading comprehension, and both logical and verbal reasoning. More than 100,000 students take the LSAT every year.

</p>


