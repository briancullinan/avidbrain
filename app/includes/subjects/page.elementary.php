<h3>Subjects of Elementary Education
</h3>
<p>
<b>Language Arts </b> –
Language arts consists of reading, writing, speaking, listening and literacy. Students learn how understand and convey messages in both verbal and written form. This includes basic reading skills, as well as an understanding of proper grammar and spelling. By learning elementary language arts, students are able to communicate more effectively.
</p><p>

<b>Arithmetic </b> –
  Elementary arithmetic is comprised of basic math and number knowledge. Students learn fundamental skills, such as counting addition, subtraction, multiplication and division. Learning such skills is extremely important for understanding more complex mathematics later in life.
</p><p>
<b>Science </b> –
Elementary science teaches students about the world around them, as well as basic methods of problem-solving. Students learn concepts such as the scientific method, fundamentals in technology and the basic building blocks of life and matter.
</p><p>
