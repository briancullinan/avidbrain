<h3>Science Tutors</h3>
<p>
A <b>Science</b>
In its broadest definition, science consists of methods that are used in order to find out more about the world and the universe as whole. Scientists typically make use of an organized problem solving technique known as the scientific method in order to prove or disprove scientific theories. This is often accomplished by testing the validity of a stated hypothesis through a formal experiment.

</p>
<p>
As an academic subject, science teaches students important problem solving skills and techniques, while also providing students with a stronger understanding of the world around them. Thus, having a basic understanding of science is useful in all facets of life. Many students go on to study science at the highest levels of academia in order to contribute to the ever changing and constantly growing scientific community.
</p>
<p>
  With many great unknowns and questions to be answered, it is unlikely that humans will ever stop taking an interest in science and the pursuit of understanding.
</p>

<h3>Branches of Science</h3>
<p>
<b>Life Sciences</b> –
 Life sciences concern the study of living organisms. This includes the study of plants, animals and humans. There are many subcategories of life sciences, such as biology, zoology, genetics and botany.
</p><p>

<b>Physics </b> –
Physics is the study of the motion of matter through spacetime. At the broadest level, physicists seek to understand how the universe behaves. In general, physicists are concerned with concepts such as mass, velocity, energy and force.
</p><p>
<b>Chemistry </b> –
Chemistry is the study of the physical properties of matter. A major part of chemistry concerns the changes that matter experiences during a chemical reaction. Scientists study chemistry in order to obtain a better understanding of the chemical makeup of the universe.
</p><p>
<p><b>Geology </b> -
Often referred to as Earth science, geology concerns the study of solid earth, including rocks, soil, sediments and other materials. Studying geology provides strong insight regarding the history of the Earth and the lifeforms that inhabit the planet.
</p>
<p><b>Astronomy </b> -
Astronomy is the study of celestial bodies and occurrences not found on Earth. This includes distant objects such as stars, galaxies and planets, as well as phenomenon such as supernovae explosions and gamma ray bursts. With the vast majority of the universe having yet to be discovered by humans, much of astronomy is based upon theory.
</p>


