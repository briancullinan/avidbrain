<h3>Major World Languages
</h3>
<p>
<b>English  </b> –
English is the official language of at least 67 countries throughout the world, including the United States. Its earliest forms have existed for more than 1,400 years. English is the third most common native language in the entire world, and it is one of the official languages of the United Nations and the European Union.

</p><p>

<b>Mandarin </b> –
Mandarin, which is also known as Standard Chinese, is the most commonly spoken language in the world. It has more than one billion native speakers, and it is the official language of both China and Taiwan. It is also an official language of the United Nations, and it is one of the four official languages of Singapore.
</p><p>
<b>Spanish </b> –
Spanish is the world’s second most popular language, with nearly 500 million native speakers worldwide. It is the official language in 20 countries, and it is one of the official languages of the United Nations. In the United States, Spanish is often used as a secondary language to English.

</p><p>
<p><b>French </b> -
French is an official language in 29 countries worldwide, and it is recognized as an official language of the United Nations. More than 200 million people worldwide speak French, with more than 80 million of them being native speakers. The origins of the French language can be traced back more than 2,000 years.
</p>

