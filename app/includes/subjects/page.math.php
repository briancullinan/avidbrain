<h3>Fields of Mathematics</h3>
<p>
<b>Arithmetic   </b> –
Arithmetic is an elementary subject of mathematics that concerns the study of quantity. It includes simple operations such as counting, addition, subtraction, multiplication and division.
</p><p>

<b>Algebra  </b> –
Algebra is a broad subcategory of mathematics that concerns the study of mathematic symbols and their operations. Students studying algebra seek to understand, apply and manipulate mathematic symbols in order to solve equations. Having a strong knowledge of algebra is essential for understanding more advanced topics in mathematics. Topics in algebra range from simple equations found in elementary algebra to more complex material found in linear algebra and abstract algebra.
</p><p>
<b>Geometry  </b> –
Geometry is a subcategory of mathematics that concerns size, shape, relative position and properties of space. Measurements such as length, width, volume and area are commonly used in geometry. Theories derived from geometry are commonly used to solve problems in high level fields of mathematics.
</p><p>
<p><b>Calculus   </b> -
Calculus is the mathematical study of change. There are two major branches of calculus, those being differential calculus and integral calculus. Calculus is commonly used to solve equations which basic algebra cannot solve alone.
</p>

