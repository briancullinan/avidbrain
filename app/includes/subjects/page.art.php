<h3>Art Tutors</h3>
<p>
Art is an extremely broad term that covers a diverse range of human activities for the purpose
 of creating visual and/or auditory artifacts, compositions or performances. Artists create
 such works in the intention that they will be able to spark the interests or invoke the
  emotions of other people. There are many different components surrounding artistic
   culture, including the production of art, the criticism of art, the dissemination of art and the history of art.
</p>
<h3>Subcategories of Art</h3>
<p>
<b>Fine Arts</b> – Fine arts refers to art that is created for the sole purpose of being seen
 and discussed by others. Works of fine art are not designed to have any practical usage
  in everyday society. Some forms of fine art include drawings, paintings, sculptures,
   photography, filmmaking and printmaking.
</p><p>
<b>Applied Arts</b> – Applied arts covers the creation of objects that are both
aesthetically-pleasing and useful in everyday society. The term applied arts
is often referred to as decorative arts. Some forms of applied art include
interior design, graphic design, industrial design and fashion.
</p><p>
<b>Visual Arts</b> – Visual arts refers to static artwork that is physically seen by others.
 Visual art can fall into the fine art category, as is the case in the visual
 mediums of drawings, paintings, sculptures, photography, filmmaking and printmaking.
 Visual art can also be considered an applied art when it involves fields
 such as architecture, interior design, graphic design, industrial design and fashion.
</p><p>
<p>
  <b>Performance Arts</b> – Performance art concerns dynamic mediums in which artists
 actively use their voices and/or the movements of their bodies in order to
  convey artistic expression. Performance arts are generally designed to be
   performed in front of a live audience. Some forms of performance art include
   dance, theatre, music, magic and puppetry.
<p>

