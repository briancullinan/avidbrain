<h3>Examples of Sports</h3>
<p>
<b>Soccer </b> –
Known in other countries as association football, soccer is a sport played between two teams in which the goal is to kick the ball into the opposing team’s goal. Soccer is the most popular sport in the world, with over 250 million athletes in more than 200 countries.
</p><p>

<b>Football  </b> –
Known as American Football outside of the United States, football is a sport played between two teams in which the goal is to advance the ball into the opposing team’s end zone or kick the ball through the opposing team’s goal post. In the United States, more than one million high school athletes and over 70,000 collegiate athletes participate in organized games every year.
</p><p>
<b>Basketball  </b> –
Basketball is a sport played between two teams in which the goal is to shoot the ball through the opposing team’s hoop. The sport requires skills such as passing, shooting, blocking, dribbling and rebounding. The sport has been a regular part of the Summer Olympic Games since 1936.
</p><p>
<p><b>Baseball  </b> -
Baseball is a bat and ball sport played between two teams in which the goal is to score runs by hitting a ball thrown by a pitcher and then running counter-clockwise around four bases. The sport has existed in some form since the 18th century, and it remains one of the most popular sports in the world to this day.
</p>
<p><b>Tennis  </b> -
Tennis is a sport played either between two individuals (singles) or between two teams of two people each (doubles). The goal is to hit the ball with a racket in such a manner that the ball remains in the area of play and that the opponent cannot properly return the ball. The sport is played by millions of individuals in more than 100 countries worldwide.
</p>

<p><b>Golf </b> -
Golf is a sport in which the goal is to hit a golf ball into a series of pre-designated holes using as few strokes as possible. The origins of golf can be traced back to 15th century Scotland. As of 2008, there are more than 17,600 active golf courses in the United States.
</p>

