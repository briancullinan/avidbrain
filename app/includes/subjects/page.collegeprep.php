<h3>College Prep Tutors</h3>
<p>
Finishing <b>college</b> has been shown to offer several tremendous advantages in today’s society.  Graduating from college offers an individual a greater number of opportunities in both initial job placement and career advancement. It has also been shown that college graduates typically earn more money during their lives than individuals who have not graduated from college.

</p>
<p>
With education being more important than ever, it’s no surprise that many young people of today are working hard in order to properly prepare for college. This is often accomplished through a college preparatory education. Such an education focuses on teaching skills that are necessary to get into college, including writing essays, preparing applications, researching schools and ensuring that required course loads are met.
</p>
<h3>Types of College Prep Programs
</h3>
<p>
<b>Federal Programs </b> –
Federal college prep programs are initiatives funded by the federal government to help young people get into college. These programs often target disadvantaged students, such as low-income individuals, aspiring first-generation college graduates and students suffering from disabilities. Federal college prep programs typically focus on high school students, but they can also focus on middle school students as well.
</p><p>

<b>State Programs </b> –
Many states such as New York, California and Florida have state-funded college preparatory programs that are designed to assist low-income and academically-challenged students in getting into college. While these programs are primarily supported by the state, such programs might also utilize federal funding as well. Many of these state programs offer personal academic support for troubled students.
</p><p>
<b>University Programs </b> –
Some college preparatory support is often offered through universities themselves. One example is the Early Academic Outreach Program from the University of California. This program provides academic enrichment, entrance exams preparation, academic advising, and college knowledge to underserved schools. Many other colleges are known for offering similar initiatives.
</p><p>
<p><b>Nonprofit Programs</b> -
Many college prep programs are offered through nonprofit companies. These companies make it their mission to help young people achieve their future educational goals. Such organizations typically focus on helping students improve their grades, while also ensuring that proper college prerequisites are met before high school graduation. Some nonprofit programs specifically focus on disadvantaged or low-income students.
<p>

