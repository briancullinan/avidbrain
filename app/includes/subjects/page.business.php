<p>
A <b>Business</b> refers to an entity that is involved in the selling of goods and/or services for the purpose of obtaining a profit or achieving certain social objectives. A business must navigate a diverse economy that is comprised of everyday consumers, other businesses and government entities in order to be successful in the world today.
</p>
<p>
Running an effective business requires several important skills such as managing people, weighing the pros and cons of complex situations, planning ahead, marketing, effective communication and maintaining a positive relation with the public. With the large number of skills required to achieve success in the business word, it’s no wonder that many people choose a background in business as the foundation of their education.
</p>
<h3>Types of Businesses</h3>
<p>
<b>Sole Proprietorship </b> – A sole proprietorship refers to a business that is owned exclusively by one individual. For legal purposes, the single owner is not considered to be separate from the business.  An advantage to a sole proprietorship is that the business owner has complete and total control over the business. A disadvantage to a sole proprietorship is that it might be harder to obtain important resources, such as money and relevant knowledge.
</p><p>
<b>Partnership</b> – A partnership is a business that has two or more owners. For legal purposes, the multiple owners are not considered to be separate from the business. An advantage to a partnership is that it is often easier to obtain important resources, such as money and relevant knowledge. A disadvantage to a partnership is that the owners do not possess exclusive and total control over the business, as they share the business with others. It should be noted that the stakes in the partnership do not have to be equal (e.g. one owner could possess 60% of the company, while another owner possess just 40%).
</p><p>
<b>Corporation</b> – A corporation is a business entity which is authorized to legally act as one individual person. This means that the business as a whole can incur debts, take legal actions and enter into contracts just like an individual. For legal purposes, the owners of the corporation possess a limited liability, which is represented by the amount of money that they have invested in the business. Corporations can be considered either public or private.
</p><p>
<p>
  A corporation that is public offers shares of stock that can be purchased by the general public through a stock market. These shares of stock represent ownership stakes in the corporation itself. A corporation that is privately-held does not offer the sale of stock to the general public. A privately-held corporation is instead owned by members of the corporation itself.
<p>

