<h3>Special Needs Tutors</h3>
<p>
A <b>Special Needs</b>
The term special needs refers to individuals who require extra assistance due to the presence of one or more medical conditions or disabilities. Special needs individuals typically possess a condition that is mental, physical or psychological in nature that makes it difficult for them to function normally in everyday society without receiving additional support. In 2005, estimates showed that 18.5% of all children under the age of 18 in the United States were considered to be special needs in one form or another.
</p>
<p>
In most cases, individuals with special needs are able to live relatively normal lives, provided that they are given the proper support and guidance. With the widespread prevalence of special needs individuals in today’s society, many tutors are dedicated to assisting those who are special needs. Given the appropriate assistance, special needs individuals can overcome their conditions and live highly successful and fulfilling lives.

</p>

<h3>Examples of Special Needs Conditions
</h3>
<p>
<b>Autism </b> –
Autism is a neurodevelopmental disorder that is usually defined by impaired social interactions, diminished communication skills and restrictive and repetitive behavior. Most autistic children are diagnosed with the condition at early age, often before they are three years old. Roughly one in every 68 children in the United States has some form of autism.
</p><p>

<b>Down Syndrome </b> –
Down syndrome is a genetic disorder caused by an individual possessing a third copy of the 21st chromosome. Individuals with Down syndrome are typically characterized by physical growth delays, distinctive facial features and intellectual disabilities. About one in every 1,000 individuals is diagnosed with Down syndrome.
</p><p>
<b>Dyslexia </b> –
A very common reading disorder, people with dyslexia experiences troubles in reading, writing, spelling and sounding out words. Individuals with dyslexia possess normal intelligence otherwise, and they can often overcome the condition with extra support and tutoring. Somewhere between 3% and 7% of all individuals are said to be dyslexic to some degree.
</p><p>
<p><b>ADHD </b> -
Attention deficit hyperactivity disorder, or ADHD, is commonly diagnosed in individuals who have trouble paying attention, demonstrate excessive energy and have difficulties controlling their behavior. Managing ADHD is usually accomplished through a combination of counseling and medication. More than 39 million people worldwide have been diagnosed with ADHD.
</p>


