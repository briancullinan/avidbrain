<h3>History Tutors</h3>
<p>
A <b>History</b>
is the study of past events and how they relate to humans and their behavior. Historians work to analyze the past in order to determine what caused historic events to take place and what effects occurred as a result of such events. By studying history, people can apply past situations to the modern world and avoid making similar mistakes and errors that other people have made in the past.
</p>
<p>
With the incredible amount of knowledge that can be gained by studying the past, it’s no wonder that many people dedicate their entire lives to studying history. Students typically start receiving lessons in history at an early age, and many continue their history studies into the highest levels of academia. At an advanced level, history is a highly specialized subject, with most history professors focusing strictly on a precise area of study.

</p>
<h3>Types of Historic Studies
</h3>
<p>
<b>Anthropology  </b> –
Anthropology is the study of humans and the societies that they live in. Anthropologists study both past and present human cultures in order to determine how humans lived, how we arrived at the present and what is most likely to happen to humans in the future.
</p><p>

<b>Comparative History  </b> –
Comparative history is a field of study that compares and contrasts different historic societies. Comparative historians typically focus on comparing societies that existed during the same time period or societies that shared similar cultural conditions. By making such comparisons, historians can analyze how different thoughts, ideologies, cultures, decisions, technologies, time periods and events played a role in shaping humans and their corresponding societies.
</p><p>
<b>Counterfactual History </b> –
Counterfactual history is a special branch of history that aims to determine what would have occurred if certain key events happened differently. By studying such hypothetical scenarios, historians can better understand exactly why certain events occurred the way that they did and how history might have changed if they had differed.

</p><p>
<p><b>Intellectual History </b> -
Intellectual history concerns the past, present and future of ideas, thinkers and philosophers. This field of history seeks to determine how human thoughts and ideologies have evolved over time. By studying intellectual history, it is possible to better understand decisions that were made in the past and predict how humans are likely to think in the future.
</p>

