<h3>Genres of Music</h3>
<p>
<b>Classical  </b> –
Classical music is form of music noted for its system of staff notation, rigid structure, distinct instrumentation, and sophistication. Common instruments used in classical music include woodwinds, brass instruments, string instruments and percussion instruments.
</p>
<p>
<b>Rock </b> –
Rock is music generally centered on the sounds of an electric guitar and accompanied by other instruments such as bass guitar and drums. Traditionally, rock music has been played in a 4/4 time signature, though many bands now experiment with alternative time signatures. Rock contains many subgenres of music, such as punk, metal and country.
</p><p>
<b>Pop </b> –
Pop is a form of popular music that is usually defined by its usage of repeated choruses, melodic tunes, hooks and short to medium song lengths. Pop music generally tries to appeal to a general audience rather than a niche sub-culture, making it appropriate for widespread listening in public settings.
</p><p>
<p><b>Hip-Hop </b> -
Hip-hop is music that is generally centered on spoken vocals that are performed to a distinctive beat, also known as rapping. Other common elements of hip-hop music include beatboxing and sampling. Hip-hop music is also known for its distinctive culture.
</p>

