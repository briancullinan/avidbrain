<h3>Basic Components of the English Language

</h3>
<p>
<b>Nouns </b> –
Simply put, a noun is a person, place, thing or idea. Nouns represent the largest classification of words in the English language.
</p><p>

<b>Verbs </b> –
A verb is a word that describes the action or the state of a noun. In English, verbs can be written in past, present or future tense to signify when the action or state takes place.
</p><p>
<b>Adjective </b> –
An adjective is a word that is used to modify or describe a noun. Adjectives provide specific details about nouns regarding "what kind", "how many" or "which one".
</p><p>
<p><b>Adverb </b> -
An adverb is a word that modifies or qualifies a verb, adjective or another adverb. Adverbs can provide details regarding matters such as speed, manner, degree, place, time, frequency, level of certainty and more.
</p>
<p>
  <b>Sentence </b> -
  A sentence is a group of multiple words that are grammatically linked. A complete sentence must tell a complete thought and consist of at least one subject and one predicate.
 </p>
 <p>
   <b>Subject </b> -
   A subject is the person, place, thing or idea that a sentence is about. In most cases, the subject is the main topic of the sentence. However, there are exceptions to this rule.
  </p>
  <p>
    <b>Predicate  </b> -
    The predicate of a sentence explains the state of the subject. Simply put, the predicate tells what is happening, what has happened or what will happen with the subject.
   </p>
