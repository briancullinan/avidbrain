<h3>How to find students</h3>
<p>Once you complete your profile, you can find student who need tutoring in two ways:</p>

<ul class="collection">
	<li class="collection-item">
		You can search the tutoring jobs board and message student through our system.
	</li>
	<li class="collection-item">
		Students may come across your profile and message you asking for help.
	</li>
	<li class="collection-item">
		Our marketplace is designed to help students find the best tutors, and that active tutors can find great leads for students.  Some things that will help you secure tutoring opportunities are your tutoring history, responsiveness and user feedback. 
	</li>
</ul>

